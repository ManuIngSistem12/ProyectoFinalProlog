    private void btnSalirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalirMouseClicked
        if (this.txtNombrePaciente.getText().isEmpty() && this.txtEdadPaciente.getText().isEmpty()) {
            this.dispose();
        } else {
            int n = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de salir (se perderan los datos ingresados)?");
            if (n == 0) {
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnSalirMouseClicked

    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        if (txtNombrePaciente.getText().isEmpty() || txtEdadPaciente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No pueden haber campos vacios");
            return;
        }
        String nombre = txtNombrePaciente.getText();
        String Sedad = txtEdadPaciente.getText();
        int edad;
        try {
            edad = Integer.parseInt(Sedad);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "la edad debe ser un numero valido ---->" + Sedad);
            return;
        }
        try{
            PacienteDAO pdao = new PacienteDAO();
            if(pdao.agregarPaciente(nombre, edad)){
                JOptionPane.showMessageDialog(this, "Se Agrego el paciente a la base de datos");
                this.dispose();
            }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "error al intentar insertar el paciente en la base de datos" + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarMouseClicked


